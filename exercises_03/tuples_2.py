# Creating a tuple named 'my_tuple' with string elements
my_tuple = ("one", "two", "three", "one")

# Counting the number of occurrences of "one" in the tuple
print("Number of occurrences of 'one':", my_tuple.count("one"))

# Finding the index of the first occurrence of "one" in the tuple
print("Index of the first occurrence of 'one':", my_tuple.index("one"))
