# Creating a list named 'my_list' with a mix of numeric and string elements
my_list = [1, 2, 3, 4, "J"]

# Getting the length of the list using the len() function
a = len(my_list)
print("Length of my_list:", a)

# Creating a slice of the list from index 1 to 2 (exclusive) with a step of 1
slice_1 = my_list[1:3:1]
print("Slice of my_list:", slice_1)

# Accessing the last element of the list using negative indexing
my_character = my_list[-1]
print("Last element of my_list:", my_character)
