# Defining a string
my_string = "This is a test message"

# Converting the string to uppercase
my_upper = my_string.upper()

# Converting the string to lowercase
my_lower = my_string.lower()

# Displaying the original, uppercase, and lowercase versions of the string
print(f"Original: {my_string}")
print(f"Upper: {my_upper}")
print(f"Lower: {my_lower}")
