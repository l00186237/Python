# Defining two numbers for division
Number = 12345
Divisor = 333

# Calculating the result of the division
Result = Number / Divisor

# Displaying the result using format()
print("Result of {} divided by {} is {}".format(Number, Divisor, Result))

# Displaying the result with a limit of 4 decimal places using format()
print("Limiting to a float with 4 decimal points would give {r:1.4f}".format(r=Result))
